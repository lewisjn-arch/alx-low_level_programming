#include "main.h"
#include <stdio.h>

/**
 * mul - This function multiplies two intergers and returns the result
 *
 * @a: This is the first value to be multiplied
 *
 * @b: This is the second value to be multiplied
 *
 * Return: Always 0
 */

int mul(int a, int b)
{
	int result;

	result = a * b;

	return (result);
}
